post_json_capture() {
  local url="$1"
  local body="$2"
  local raw
  raw="$(curl -sS -H 'Content-Type: application/json' -d "$body" -w $'\n%{http_code}' "$url")"
  HTTP_STATUS="${raw##*$'\n'}"
  HTTP_BODY="${raw%$'\n'*}"
}

post_json_ok() {
  local url="$1"
  local body="$2"
  HTTP_BODY="$(curl -fsS -H 'Content-Type: application/json' -d "$body" "$url")"
}

assert_capture_payload() {
  JSON_PAYLOAD="$1" "$PYTHON_BIN" - "$2" <<'PY'
import json
import os
import sys

payload = json.loads(os.environ["JSON_PAYLOAD"])
session_id = sys.argv[1]
if payload.get("code") != 0:
    raise SystemExit("capture response code was not 0")
event = payload["event"]
observation = payload["observation"]
if event.get("type") != "pet.control":
    raise SystemExit("capture event type was not pet.control")
if observation.get("type") != "pet.observation":
    raise SystemExit("capture observation type was not pet.observation")
if event.get("session_id") != session_id or observation.get("session_id") != session_id:
    raise SystemExit("capture session_id mismatch")
event_privacy = event["privacy"]
observation_privacy = observation["privacy"]
if event_privacy.get("raw_frame_sent") is not False:
    raise SystemExit("event privacy.raw_frame_sent was not false")
if observation_privacy.get("raw_frame_sent") is not False:
    raise SystemExit("observation privacy.raw_frame_sent was not false")
if observation_privacy.get("retention") != "none":
    raise SystemExit("observation privacy.retention was not none")
vision = observation["vision"]
if not vision.get("enabled"):
    raise SystemExit("capture vision.enabled was not true")
if vision.get("source") != "uploaded_frame":
    raise SystemExit(f"capture vision.source was {vision.get('source')!r}, expected uploaded_frame")
frame = vision.get("frame") or {}
if int(frame.get("width") or 0) <= 0 or int(frame.get("height") or 0) <= 0:
    raise SystemExit("capture frame dimensions were missing")
scene = vision.get("scene") or {}
if not scene.get("summary"):
    raise SystemExit("capture scene.summary was missing")
print("[pet-vision-smoke] upload capture privacy/frame/scene contract OK")
