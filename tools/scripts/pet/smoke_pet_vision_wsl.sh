#!/usr/bin/env bash
set -euo pipefail

BASE_URL="http://127.0.0.1:8096/pet"
IMAGE_FILE=""
UID_VALUE="7"
UID_EXPLICIT="false"
PROFILE_ID="default"
PERSONA="memo-pet"
PROVIDER="scripted"
PYTHON_BIN="${PYTHON_BIN:-python3}"
FULL_VISUAL_LAYER="false"
KEEP_GENERATED_FRAME="false"
ALLOW_CAPTURE_DISABLED="false"
GENERATED_DIR=""

cleanup() {
  if [[ -n "$GENERATED_DIR" && "$KEEP_GENERATED_FRAME" != "true" ]]; then
    case "$GENERATED_DIR" in
      /tmp/memochat-pet-vision.*)
        rm -rf -- "$GENERATED_DIR"
        ;;
    esac
  fi
}
trap cleanup EXIT

usage() {
  cat <<'EOF'
Usage: smoke_pet_vision_wsl.sh [--base-url URL] [--image-file PATH] [--full-visual-layer]

Checks AIOrchestrator pet vision diagnostics. When --image-file is provided,
creates a pet session and uploads one PNG/JPEG frame to /pet/sessions/{id}/capture.
With --full-visual-layer, creates a synthetic test user and validates diagnostics,
uploaded-frame capture, rich visual observation, agent input, visual boundary cases,
session listing, and interrupt behavior.

Options:
  --base-url URL              Pet API base URL, default http://127.0.0.1:8096/pet
  --image-file PATH           PNG/JPEG file to base64 upload for capture smoke
  --full-visual-layer         Run the full synthetic test-user visual layer flow
  --allow-capture-disabled    Continue boundary/input checks if /capture returns 503
  --keep-generated-frame      Keep the synthetic PNG generated by --full-visual-layer
  --uid UID                   Session uid, default 7, or 909007 with --full-visual-layer
  --profile-id ID             Session profile id, default "default"
  --help                      Show this help
EOF
}

while [[ $# -gt 0 ]]; do
  case "$1" in
    --base-url)
      BASE_URL="${2:?missing --base-url value}"
      shift 2
      ;;
    --image-file)
      IMAGE_FILE="${2:?missing --image-file value}"
      shift 2
      ;;
    --uid)
      UID_VALUE="${2:?missing --uid value}"
      UID_EXPLICIT="true"
      shift 2
      ;;
    --profile-id)
      PROFILE_ID="${2:?missing --profile-id value}"
      shift 2
      ;;
    --full-visual-layer)
      FULL_VISUAL_LAYER="true"
      shift
      ;;
    --allow-capture-disabled)
      ALLOW_CAPTURE_DISABLED="true"
      shift
      ;;
    --keep-generated-frame)
      KEEP_GENERATED_FRAME="true"
      shift
      ;;
    --help|-h)
      usage
      exit 0
      ;;
    *)
      echo "Unknown argument: $1" >&2
      usage >&2
      exit 2
      ;;
  esac
done

require_cmd() {
  if ! command -v "$1" >/dev/null 2>&1; then
    echo "Missing required command: $1" >&2
    exit 2
  fi
}

require_cmd curl
require_cmd "$PYTHON_BIN"

BASE_URL="${BASE_URL%/}"
if [[ "$FULL_VISUAL_LAYER" == "true" && "$UID_EXPLICIT" != "true" ]]; then
  UID_VALUE="909007"
fi

print_json() {
  if ! printf '%s\n' "$1" | "$PYTHON_BIN" -m json.tool; then
    printf '%s\n' "$1"
  fi
}

make_session_body() {
  "$PYTHON_BIN" - "$UID_VALUE" "$1" "$PERSONA" "$PROVIDER" <<'PY'
import json
import sys

uid, profile_id, persona, provider = sys.argv[1:5]
print(json.dumps({
    "uid": int(uid),
    "profile_id": profile_id,
    "persona": persona,
    "provider": provider,
}, ensure_ascii=False))
PY
}

extract_session_id() {
  JSON_PAYLOAD="$1" "$PYTHON_BIN" - <<'PY'
import json
import os

print(json.loads(os.environ["JSON_PAYLOAD"])["session"]["session_id"])
PY
}

create_session() {
  local profile_id="$1"
  local create_body
  create_body="$(make_session_body "$profile_id")"

  echo "[pet-vision-smoke] POST ${BASE_URL}/sessions (${profile_id}, uid=${UID_VALUE})"
  SESSION_RESPONSE="$(curl -fsS -H 'Content-Type: application/json' -d "$create_body" "${BASE_URL}/sessions")"
  print_json "$SESSION_RESPONSE"
  SESSION_ID="$(extract_session_id "$SESSION_RESPONSE")"
}

generate_synthetic_png() {
  local output_path="$1"
  "$PYTHON_BIN" - "$output_path" <<'PY'
import binascii
import math
import struct
import sys
import zlib
from pathlib import Path

path = Path(sys.argv[1])
width, height = 320, 240
rows = []

def inside_circle(x, y, cx, cy, radius):
    return (x - cx) ** 2 + (y - cy) ** 2 <= radius ** 2

for y in range(height):
    row = bytearray()
    for x in range(width):
        r = int(232 + 12 * x / width)
        g = int(238 + 10 * y / height)
        b = 242
        if inside_circle(x, y, 150, 92, 54):
            r, g, b = 246, 204, 172
        if inside_circle(x, y, 130, 82, 6) or inside_circle(x, y, 170, 82, 6):
            r, g, b = 34, 42, 52
        if 128 <= x <= 174 and 112 <= y <= 130:
            curve = 120 + 6 * math.sin((x - 128) / 46 * math.pi)
            if abs(y - curve) <= 2:
                r, g, b = 190, 69, 86
        if 38 <= x <= 76 and 146 <= y <= 196:
            r, g, b = 90, 157, 214
        if 76 <= x <= 92 and 160 <= y <= 184:
            r, g, b = 70, 130, 184
        if 178 <= x <= 294 and 168 <= y <= 206:
            r, g, b = 44, 53, 65
        if 186 <= x <= 284 and 176 <= y <= 196 and (x // 10 + y // 8) % 2 == 0:
            r, g, b = 70, 81, 96
        row.extend((r, g, b))
    rows.append(b"\x00" + bytes(row))

def chunk(kind, data):
    crc = binascii.crc32(kind + data) & 0xFFFFFFFF
    return struct.pack(">I", len(data)) + kind + data + struct.pack(">I", crc)

raw = b"".join(rows)
png = (
    b"\x89PNG\r\n\x1a\n"
    + chunk(b"IHDR", struct.pack(">IIBBBBB", width, height, 8, 2, 0, 0, 0))
    + chunk(b"IDAT", zlib.compress(raw, 9))
    + chunk(b"IEND", b"")
)
path.write_bytes(png)
print(path)
PY
}

make_capture_body() {
  "$PYTHON_BIN" - "$1" <<'PY'
import base64
import json
import struct
import sys
from pathlib import Path

path = Path(sys.argv[1])
data = path.read_bytes()
mime = "application/octet-stream"
width = 0
height = 0
if data.startswith(b"\x89PNG\r\n\x1a\n") and len(data) >= 24:
    mime = "image/png"
    width, height = struct.unpack(">II", data[16:24])
elif data.startswith(b"\xff\xd8"):
    mime = "image/jpeg"
    i = 2
    while i + 9 < len(data):
        while i < len(data) and data[i] == 0xff:
            i += 1
        marker = data[i] if i < len(data) else 0
        i += 1
        if marker in {0xc0, 0xc1, 0xc2, 0xc3, 0xc5, 0xc6, 0xc7, 0xc9, 0xca, 0xcb, 0xcd, 0xce, 0xcf}:
            height, width = struct.unpack(">HH", data[i + 3:i + 7])
            break
        if i + 2 > len(data):
            break
        segment_len = struct.unpack(">H", data[i:i + 2])[0]
        i += max(segment_len, 2)
print(json.dumps({
    "frame_base64": base64.b64encode(data).decode("ascii"),
    "frame_mime": mime,
    "frame_width": width,
    "frame_height": height,
    "include_frame": False,
    "metadata": {"source": "uploaded_frame", "file_name": path.name, "test_user": True},
}, ensure_ascii=False))
PY
}

