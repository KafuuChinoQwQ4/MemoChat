assert_visual_observation_payload() {
  JSON_PAYLOAD="$1" "$PYTHON_BIN" - "$2" "$3" <<'PY'
import json
import os
import sys

payload = json.loads(os.environ["JSON_PAYLOAD"])
session_id, expected = sys.argv[1:3]
if payload.get("code") != 0:
    raise SystemExit("observation response code was not 0")
event = payload["event"]
if event.get("type") != "pet.control" or event.get("session_id") != session_id:
    raise SystemExit("observation event contract mismatch")
summary = event.get("debug", {}).get("visual_summary", {})
speech = event.get("speech", {}).get("text_delta", "")
if event.get("privacy", {}).get("raw_frame_sent") is not False:
    raise SystemExit("observation event raw_frame_sent was not false")
if expected == "high":
    if event.get("action", {}).get("name") != "visual_react":
        raise SystemExit("high-confidence observation did not visual_react")
    if not summary.get("speak"):
        raise SystemExit("high-confidence observation did not speak")
    if "我看到你啦" not in speech or "心情不错" not in speech:
        raise SystemExit("first detected user contextual greeting was not emitted")
    if summary.get("reason") != "first_user_seen":
        raise SystemExit("high-confidence first observation did not use first_user_seen")
    if "杯子和键盘" not in str(summary.get("summary_text") or ""):
        raise SystemExit("high-confidence scene summary was not propagated")
    if event.get("expression") != "smile_soft":
        raise SystemExit("high-confidence expression did not map to smile_soft")
    if event.get("gaze", {}).get("target") != "user_face":
        raise SystemExit("high-confidence gaze target was not user_face")
    if float(event.get("lip_sync", {}).get("value") or 0.0) <= 0.0:
        raise SystemExit("high-confidence visual lip sync was not raised")
else:
    if summary.get("reason") != expected:
        raise SystemExit(f"expected visual_summary.reason={expected}, got {summary.get('reason')!r}")
    if summary.get("speak"):
        raise SystemExit(f"{expected} boundary unexpectedly spoke")
    if speech:
        raise SystemExit(f"{expected} boundary emitted speech text")
print(f"[pet-vision-smoke] visual observation boundary OK: {expected}")
PY
}

