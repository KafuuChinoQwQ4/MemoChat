echo "[pet-vision-smoke] GET ${BASE_URL}/diagnostics/vision"
diagnostics_body="$(curl -fsS "${BASE_URL}/diagnostics/vision")"
print_json "$diagnostics_body"

if [[ "$FULL_VISUAL_LAYER" != "true" && -z "$IMAGE_FILE" ]]; then
  echo "[pet-vision-smoke] no --image-file supplied; diagnostics-only smoke complete"
  exit 0
fi

if [[ "$FULL_VISUAL_LAYER" == "true" && -z "$IMAGE_FILE" ]]; then
  GENERATED_DIR="$(mktemp -d /tmp/memochat-pet-vision.XXXXXX)"
  IMAGE_FILE="${GENERATED_DIR}/synthetic-test-user.png"
  echo "[pet-vision-smoke] generate synthetic frame ${IMAGE_FILE}"
  generate_synthetic_png "$IMAGE_FILE" >/dev/null
fi

if [[ ! -f "$IMAGE_FILE" ]]; then
  echo "Image file not found: $IMAGE_FILE" >&2
  exit 2
fi

if [[ "$FULL_VISUAL_LAYER" == "true" ]]; then
  capture_profile="vision-smoke-capture"
  agent_profile="vision-smoke-agent"
else
  capture_profile="$PROFILE_ID"
  agent_profile="$PROFILE_ID"
fi

create_session "$capture_profile"
capture_session_id="$SESSION_ID"
capture_body="$(make_capture_body "$IMAGE_FILE")"

echo "[pet-vision-smoke] POST ${BASE_URL}/sessions/${capture_session_id}/capture (--image-file)"
post_json_capture "${BASE_URL}/sessions/${capture_session_id}/capture" "$capture_body"
if [[ "$HTTP_STATUS" != "200" ]]; then
  print_json "$HTTP_BODY"
  if [[ "$FULL_VISUAL_LAYER" == "true" && "$ALLOW_CAPTURE_DISABLED" == "true" && "$HTTP_STATUS" == "503" ]]; then
    echo "[pet-vision-smoke] capture returned 503; continuing because --allow-capture-disabled was supplied"
  else
    echo "[pet-vision-smoke] capture failed with HTTP ${HTTP_STATUS}" >&2
    exit 1
  fi
else
  print_json "$HTTP_BODY"
  assert_capture_payload "$HTTP_BODY" "$capture_session_id"
  segment_body="$(make_segment_capture_body "$IMAGE_FILE")"
  echo "[pet-vision-smoke] POST ${BASE_URL}/sessions/${capture_session_id}/capture-segment (--image-file keyframes)"
  post_json_capture "${BASE_URL}/sessions/${capture_session_id}/capture-segment" "$segment_body"
  if [[ "$HTTP_STATUS" != "200" ]]; then
    print_json "$HTTP_BODY"
    echo "[pet-vision-smoke] segment capture failed with HTTP ${HTTP_STATUS}" >&2
    exit 1
  fi
  print_json "$HTTP_BODY"
  assert_segment_capture_payload "$HTTP_BODY" "$capture_session_id"
fi

if [[ "$FULL_VISUAL_LAYER" != "true" ]]; then
  exit 0
fi

create_session "$agent_profile"
agent_session_id="$SESSION_ID"

for observation_kind in high cooldown low_confidence no_face; do
  observation_body="$(make_visual_observation_body "$observation_kind")"
  echo "[pet-vision-smoke] POST ${BASE_URL}/sessions/${agent_session_id}/observation (${observation_kind})"
  post_json_ok "${BASE_URL}/sessions/${agent_session_id}/observation" "$observation_body"
  print_json "$HTTP_BODY"
  if [[ "$observation_kind" == "cooldown" ]]; then
    assert_visual_observation_payload "$HTTP_BODY" "$agent_session_id" "cooldown"
  else
    assert_visual_observation_payload "$HTTP_BODY" "$agent_session_id" "$observation_kind"
